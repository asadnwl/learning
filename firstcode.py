# Declaring variables
myNumber = 10       # Int
print(myNumber)
myNumber = 10.5    # float
print(myNumber)
myNumber = 10j      # complex
print(myNumber)
myNumber = "Asad"   # string
print(myNumber)
x = True            # boolen
print(x)
x = b"Asad"         # binary
print(x)
x = ["Muhammad", "Asad", "Muhammad", "Iqran"]       # list
print(x)
x = ("Muhammad", "Asad", "Muhammad", "Iqran")       # tuple
print(x)
x = {"Muhammad", "Asad", "Muhammad", "Iqran"}       # set
print(x)
x = {"name": "Asad", "age" : 25}                    # dictionary
print(x)
